package com.frame.saeasyandroids.json;

/**
 * 常量类：辅助验证FastJson使用
 * 
 * @author zhangshun
 */
class User{
	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}